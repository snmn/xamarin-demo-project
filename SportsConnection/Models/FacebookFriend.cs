namespace SportsConnection {
	
	public class FacebookFriend {

		private string mFacebookId;
		private string mName;

		public string facebookId {
			get {
				return mFacebookId;
			}
			set {
				mFacebookId = value;
			}
		}

		public string name {
			get {
				return mName;
			}
			set {
				mName = value;
			}
		}

	}

}
